int		ft_antidote(int i, int j, int k)
{
	int check;

	check = 0;
	if ((j > i && i > k) || (j < i && i < k))
		check = j;
	if ((i > j && j > k) || (i < j && j < k))
		check = j;
	if ((i > k && k > j) || (i < k && k < j))
		check = k;
	return (check);
}
