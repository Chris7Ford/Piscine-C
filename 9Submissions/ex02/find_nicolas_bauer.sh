INFO=`cat $1 | grep -i 'bauer' | grep -i '^nicolas\tbauer'`

for i in $INFO; do
	j=`echo "$i" | sed 's/[^0-9]//g'`
	if [ ${#j} -ge 9 ]; then
		echo "$j"
	fi
done
