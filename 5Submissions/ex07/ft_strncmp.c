int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;

	if (n == 0)
		return (0);
	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && i < n)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		else if ((s1[i + 1] == '\0' && s2[i + 1] == '\0') || (i + 1) == n)
			return (0);
		i++;
	}
	if (s1[i] != '\0')
		return (s1[i]);
	else
		return ((s2[i]) * -1);
}
