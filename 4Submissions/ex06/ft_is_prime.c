#include <stdio.h>

int		ft_is_prime(int nb)
{
	int i;

	i = nb - 1;
	if (nb == 1 || nb == 0)
		return (0);
	else
	{
		while (i > 1)
		{
			if (nb % i == 0)
				return (0);
			i--;
		}
	}
	return (1);
}
