#include <unistd.h>

void	inter(char *s1, char *s2)
{
	int		i;
	int		j;

	i = -1;
	while (s1[++i])
	{
		j = -1;
		while (++j < i)
		{
			if (s1[i] == s1[j])
				break ;
		}
		if (j == i)
		{
			j = -1;
			while (s2[++j])
			{
				if (s1[i] == s2[j])
				{
					write(1, &(s1[i]), 1);
					break ;
				}
			}
		}
	}
}
